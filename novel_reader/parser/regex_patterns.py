"""正则模式"""

import re

# 预处理：去除控制字符和HTML标签
CLEAN_CONTROL_CHARS = re.compile(r'[\u0001-\u0009\u001B\u001C\u001E\u001F\u007F]')
CLEAN_HTML_TAGS = re.compile(r'((<|(&lt;))[a-z/!\u002D][\u0000-\u003D\u003F-\uFFFF]{0,200}(>|(&gt;)))', re.IGNORECASE)

# 卷名检测（优先于章节）
# 使用非贪婪匹配，确保"第一卷 xxx"这种短卷名也能正确匹配
VOLUME_PATTERN = re.compile(r'^[\u0020\u3000\t\n\r]*(第[^第\n\r]*?卷)[\u0020\u3000\t]*[^\n\r]*')

# 章节/序言/后记/书名 匹配
# 扩展支持：允许章节标题后更长内容，支持更多格式
CHAPTER_MAIN_PATTERN = re.compile(
    r'^[\u0020\u3000\t\n\r]*'
    r'(?:'
    r'(《[^\n\r]+》)'
    r'|(序|序[\u0020\u3000\t]*章|序[\u0020\u3000\t]*言|前[\u0020\u3000\t]*言|引[\u0020\u3000\t]*言|引[\u0020\u3000\t]*子|摘[\u0020\u3000\t]*要|楔[\u0020\u3000\t]*子|背景简介|内容简介|楔子)'
    r'|((?:第[\u0020\u3000\t]*)?(?:(?:[0-9]+(?:\.[0-9]+)?)|(?:[〇零一壹二贰三叁四肆五伍六陆七柒八捌九玖十拾百佰千仟万亿萬億廿卅卌]+))[\u0020\u3000\t]*([章节回折篇幕集卷])(?:[\u0020\u3000\t：:，,、\-—·\u00B7][^\n\r]{0,200})?)'
    r'|(后[\u0020\u3000\t]*记|附[\u0020\u3000\t]*言|结[\u0020\t]*语|尾声|大结局)'
    r'|([^\n\r]+)'
    r')'
    r'$'
)

# 中文数字提取
CN_NUM_IN_CHAPTER = re.compile(
    r'^[第\s]*(?:(?:第\s*)?([零一二三四五六七八九十百千万亿壹贰叁肆伍陆柒捌玖拾佰仟萬億廿卅卌]+)\s*([章节回折篇幕集]))\s*(.*)'
)